package nutrition.model;

public class FoodmarkBean {

	private int fnum;
	private String id;
	private int fmnum;

	public int getFnum() {
		return fnum;
	}

	public void setFnum(int fnum) {
		this.fnum = fnum;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getFmnum() {
		return fmnum;
	}

	public void setFmnum(int fmnum) {
		this.fmnum = fmnum;
	}

}
