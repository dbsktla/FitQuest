package nutrition.model;

public class NutritionBean {

	private int nnum;
	private String id;
	private String nudate;

	public int getNnum() {
		return nnum;
	}

	public void setNnum(int nnum) {
		this.nnum = nnum;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNdate() {
		return nudate;
	}

	public void setNdate(String nudate) {
		this.nudate = nudate;
	}

}
