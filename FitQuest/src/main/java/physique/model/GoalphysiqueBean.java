package physique.model;

public class GoalphysiqueBean {

	private String id;
	private String gpdate;
	private double weight;
	private double bodyfatper;
	private double goaltime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getGpdate() {
		return gpdate;
	}

	public void setGpdate(String gpdate) {
		this.gpdate = gpdate;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}

	public double getBodyfatper() {
		return bodyfatper;
	}

	public void setBodyfatper(double bodyfatper) {
		this.bodyfatper = bodyfatper;
	}

	public double getGoaltime() {
		return goaltime;
	}

	public void setGoaltime(double goaltime) {
		this.goaltime = goaltime;
	}

}
